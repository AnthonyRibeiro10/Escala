// Função para buscar e exibir as informações da escala
function buscarEscala() {
    const musicoSelecionado = document.getElementById('musico').value;
    const diaSelecionado = document.getElementById('dia').value;
    if (diaSelecionado === 'todos') {
        exibirTodasAsEscalas(musicoSelecionado);
    } else {
        exibirInformacoes(musicoSelecionado, diaSelecionado);
    }
}

// Função para exibir as informações de todas as escalas para o músico selecionado
function exibirTodasAsEscalas(musico) {
    const infoMusico = document.getElementById('infoMusico');
    infoMusico.innerHTML = '';

    // Percorre todos os dias da semana
    const diasSemana = ["sabados", "domingos", "quintas","todos"]; // Adicione outros dias aqui, se necessário
    for (const dia of diasSemana) {
        const escalasDia = dados[dia];
        for (const escalaDia of escalasDia) {
            const musicosEscala = Object.values(escalaDia).filter(item => typeof item === 'string');
            if (musicosEscala.includes(musico) || musicosEscala.some(item => item.includes(`(${musico})`) || item.includes(musico))) {
                const divEscala = document.createElement('div');
                divEscala.classList.add('escala-item');
                divEscala.innerHTML = `<h2>${escalaDia.Data}</h2>`;

                for (const [instrumento, musicoEscalado] of Object.entries(escalaDia)) {
                    const isSelecionado = musicoEscalado.includes(musico) || musicoEscalado.includes(`(${musico})`);
                    const isRepertorio = musicoEscalado.includes('(Repertório)');
                    const isMicAzul = musicoEscalado.includes('(Mic Azul)');

                    let classeSelecionado = '';
                    if (isSelecionado) {
                        classeSelecionado = 'musico-selecionado';
                    } else if (isRepertorio) {
                        classeSelecionado = 'musico-repertorio';
                    } else if (isMicAzul) {
                        classeSelecionado = 'musico-mic-azul';
                    }

                    divEscala.innerHTML += `<p class="${classeSelecionado}">${instrumento}: ${musicoEscalado}</p>`;
                }

                infoMusico.appendChild(divEscala);
            }
        }
    }
}

